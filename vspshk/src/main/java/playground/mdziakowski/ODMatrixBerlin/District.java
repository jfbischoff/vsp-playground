package playground.mdziakowski.ODMatrixBerlin;

public class District {
	
	private String name;
	private int id;
	
	public District(String name, int id) {
		this.name = name;
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public int getId() {
		return id;
	}

}
